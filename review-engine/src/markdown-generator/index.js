/**
 * Generates a markdown table of reviewed files.
 */
function generateFilesTable(files) {
  if (!files || files.length === 0) return '> No files to display.\n\n';

  let md = `| File | Resource Type | Change | Findings |\n`;
  md += `|------|--------------|--------|----------|\n`;

  for (const f of files) {
    const icon = f.resourceMeta?.icon || '📄';
    const type = f.resourceMeta?.label || f.resourceType || 'Unknown';
    const changeIcon = f.changeType === 'added' ? '🟢 Added' : f.changeType === 'modified' ? '🟡 Modified' : '🔴 Deleted';
    const findingCount = f.findings ? f.findings.length : 0;
    const findingLabel = findingCount > 0 ? `⚠️ ${findingCount}` : '✅ 0';
    md += `| ${icon} \`${f.filePath}\` | ${type} | ${changeIcon} | ${findingLabel} |\n`;
  }

  md += `\n`;
  return md;
}

/**
 * Generates detailed findings section with AI-style analysis.
 */
function generateFindingsSection(findings) {
  if (!findings || findings.length === 0) return '';

  // Group by severity
  const grouped = {};
  for (const f of findings) {
    if (!grouped[f.severity]) grouped[f.severity] = [];
    grouped[f.severity].push(f);
  }

  let md = '';

  for (const level of ['CRITICAL', 'HIGH', 'MEDIUM', 'LOW']) {
    const group = grouped[level];
    if (!group || group.length === 0) continue;

    const levelConfig = config.severity.levels[level];
    md += `### ${levelConfig.color} ${level} (${group.length})\n\n`;

    for (const f of group) {
      md += `#### ${levelConfig.color} ${f.title}\n\n`;
      md += `| Field | Details |\n`;
      md += `|-------|--------|\n`;
      md += `| **Severity** | ${level} |\n`;
      md += `| **Category** | ${f.category} |\n`;
      md += `| **File** | \`${f.file || 'N/A'}\` |\n`;
      md += `| **Resource** | ${f.resource || 'N/A'} |\n\n`;

      md += `**Description:**  \n${f.description}\n\n`;
      md += `**Impact:**  \n${f.impact}\n\n`;
      md += `**Recommendation:**  \n${f.recommendation}\n\n`;

      if (f.evidence) {
        md += `**Evidence:**\n\`\`\`\n${f.evidence}\n\`\`\`\n\n`;
      }

      md += `---\n\n`;
    }
  }

  return md;
}

/**
 * Generates recommendations section with governance insights.
 */
function generateRecommendationsSection(findings) {
  if (!findings || findings.length === 0) {
    return '> 🎉 No actionable recommendations. Configuration and code look secure.\n\n';
  }

  let md = '';

  // Deduplicate recommendations
  const seen = new Set();
  const uniqueRecs = [];
  for (const f of findings) {
    const key = `${f.id}-${f.recommendation}`;
    if (!seen.has(key)) {
      seen.add(key);
      uniqueRecs.push(f);
    }
  }

  // Sort by severity
  uniqueRecs.sort((a, b) => severityOrder(a.severity) - severityOrder(b.severity));

  let counter = 1;
  for (const f of uniqueRecs) {
    const icon = config.severity.levels[f.severity]?.color || '❓';
    md += `${counter}. ${icon} **${f.title}** — ${f.recommendation}\n`;
    counter++;
  }

  md += `\n`;
  return md;
}

/**
 * Generates Auth0 best practices checklist.
 */
function generateBestPracticesChecklist(codeResult) {
  let md = '';

  const checks = [
    { label: 'Auth0 Action Secrets used for credentials', pattern: /event\.secrets\./i },
    { label: 'Proper error handling (try/catch)', pattern: /try\s*\{/i },
    { label: 'Access denial patterns (api.access.deny)', pattern: /api\.access\.deny/i },
    { label: 'MFA enforcement (api.multifactor.enable)', pattern: /api\.multifactor\.enable/i },
    { label: 'Custom claims (api.idToken/accessToken.setCustomClaim)', pattern: /api\.(?:idToken|accessToken)\.setCustomClaim/i },
    { label: 'No eval() or dynamic code execution', pattern: /\beval\s*\(/i, negate: true },
    { label: 'No hardcoded secrets', pattern: /(?:password|secret|token)\s*[:=]\s*['"`][^'"`]{8,}/i, negate: true },
  ];

  for (const check of checks) {
    let found = false;
    for (const file of codeResult.reviewedFiles) {
      if (file.content && check.pattern.test(file.content)) {
        found = true;
        break;
      }
    }

    if (check.negate) {
      md += found ? `- ⚠️ ${check.label}\n` : `- ✅ ${check.label}\n`;
    } else {
      md += found ? `- ✅ ${check.label}\n` : `- ⬜ ${check.label} (not detected)\n`;
    }
  }

  md += `\n`;
  return md;
}

/**
 * Returns numeric order for severity (lower = more severe).
 */
function severityOrder(severity) {
  const order = { CRITICAL: 0, HIGH: 1, MEDIUM: 2, LOW: 3 };
  return order[severity] ?? 4;
}

